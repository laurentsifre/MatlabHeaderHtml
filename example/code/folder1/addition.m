% ADDITION Compute the sum of two number
%
% Usage
%   z = ADDITION(x, y)
%
% Input
%   x (numeric): the first number 
%   y (numeric): the second number
%
% Output
%   z (numeric): the sum of x and y
%
% Description
%   Compute the sum of two number using MATLAB built-in '+' function.
%	Also works with vector or matrix of same size !
%
% See also
%   SUBSTRACTION
%
function z = addition(x, y)
	z = x + y;
end
