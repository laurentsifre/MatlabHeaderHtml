% SUBSTRACTION Compute the difference two numbers
%
% Usage
%   z = SUBSTRACTION(x, y)
%
% Input
%   x (numeric): the first number 
%   y (numeric): the second number
%
% Output
%   z (numeric): the differnece between  x and y
%
% Description
%   Compute the difference of two number using MATLAB built-in '-' function.
%	Also works with vector or matrix of same size !
%
% See also
%   ADDITION
%
function z = substraction(x, y)
	z = x - y;
end

